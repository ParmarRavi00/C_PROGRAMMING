// Substraction of Two Numbers

#include<stdio.h>

void main()
{
	int val1, val2, sum;
	
	printf("Please Enter First Value :- ");
	
	scanf("%d",&val1);
	
	printf("\n Please Second value :- ");
	scanf("%d",&val2);
	
	sum = val1 - val2;
		
	printf("\n Substraction of First and Second Values is :-  %d",sum);
	
}